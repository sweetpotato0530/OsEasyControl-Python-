import os

def nt():
     os.system('pause')
     os.system('cls')

def next_text():
     os.system('pause')
     os.system('cls')

def rs():
     os.system('color 0f')
     os.system('cls')

def reset_screen():
     os.system('color 0f')
     os.system('cls')

def to():
     os.system('shutdown -s')

def turn_off():
     os.system('shutdown -s')

def rc():
     os.system('shutdown -r')

def restart_computer():
     os.system('shutdown -r')

def rcit(a):
     os.system('shutdown -r -t '+ str(a) +'')

def restart_computer_in_time(a):
     os.system('shutdown -r -t '+ str(a) +'')

def toit(a):
     os.system('shutdown -s -t '+ str(a) +'')

def turn_off_in_time(a):
     os.system('shutdown -s -t '+ str(a) +'')

def cp(a):
     os.system('ping'+ str(a) +'')

def check_ping(a):
     os.system('ping'+ str(a) +'')

def bs():
     os.system('color 1f')

def blue_screen():
     os.system('color 1f')

